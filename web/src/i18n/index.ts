function detectLocale(): string {
  const stored = localStorage.getItem('zaga_locale');
  if (stored && translations[stored]) return stored;
  const nav = navigator.language.split('-')[0];
  return translations[nav] ? nav : 'en';
}

let currentLocale = detectLocale();

export function setLocale(locale: string) {
  if (translations[locale]) {
    currentLocale = locale;
    localStorage.setItem('zaga_locale', locale);
  }
}

export function getLocale(): string {
  return currentLocale;
}

export function t(key: string, ...args: (string | number)[]): string {
  const val = translations[currentLocale]?.[key] ?? translations['en']?.[key] ?? key;
  if (args.length === 0) return val;
  return args.reduce<string>((s, a, i) => s.replace(`{${i}}`, String(a)), val);
}

export function getAvailableLocales(): string[] {
  return Object.keys(translations);
}
